from collections import deque

def threeSteps(q, x):
  if len(q) < x:
    x = len(q)
  maxVal = q[0]
  for _ in range(x):
    i = q.popleft()
    if i[0] > maxVal[0]:
      maxVal = i
    if i[0] > 0:
      i = (i[0]-1, i[1])
    q.append(i)
  return (maxVal[0]-1,maxVal[1]) if maxVal[0] > 0 else maxVal



def findPositions(arr, x):
  que = deque([(v,i+1) for i, v in enumerate(arr)])
  res = []
  for _ in range(x):
    r = threeSteps(que, x)
    que.remove(r)
    res.append(r[1])
  return res
  
  

	









