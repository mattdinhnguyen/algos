def matching_pairs(s, t):
  swapMap = dict()
  swapped = False
  matches = 0
  for i,c in enumerate(s):
    if c == t[i]:
      matches += 1
    elif not swapped:
      if (t[i],c) in swapMap:
        matches += 2
        swapped = True
      else:
        swapMap[(c,t[i])] = i
  if not swapped and len(swapMap) < 2:
    matches -= 2

  return matches

